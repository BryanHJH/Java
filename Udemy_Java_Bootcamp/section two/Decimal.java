public class Decimal {
    public static void main(String[] args) {
        double percentage = 55.7;
        System.out.println("In 2020, " + percentage + "% of the world's population is urban.");

        //Storing a whole number as a double
        double dividend = 25;
        double divisor = 2;
        System.out.println("Dividend before division: " + dividend);
        System.out.println("Divided dividend: " + dividend/divisor);
    }
}
